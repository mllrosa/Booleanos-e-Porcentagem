#operadores booleanos são comparativos que retornam valores de verdadeiro(true) ou falso(false)

a = 3 > 4
print(a) #saída #false

# != (diferente)
b = 7 != 7
print(b) #Saída #False

# sinal de igualdade em python é ==
igual = 10 == 10
print(igual) #Saída #True

#comprarativo composto
composto = 9 == 10 - 1
print(composto) #Saída #True

composto2 = 5 + 4 <= 11 - 2
print(composto2) #Saída #True

composto3 = 6 >=  5 + 1
print(composto3) #Saída #True
